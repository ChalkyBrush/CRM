require ("./database")

class Contact
  def initialize(first_name, last_name, email, note)
    @first_name = first_name
    @last_name = last_name
    @email = email
    @note = note
    Database.add_contact([@first_name,@last_name,@email,@note])
  end



  def first_name
    @first_name
  end

  def last_name
    @last_name
  end

  def email
    @email
  end

  def note
    @note
  end
end


database = Database.new